package backend;

public enum Cardinal {
		
		NORTH(0,1), SOUTH(0,-1), WEST(-1,0), EAST(1,0);
		
		private int dirX;
		private int dirY;
		
		private Cardinal(int dirX, int dirY) {
			this.dirX = dirX;
			this.dirY = dirY;
		}
		
		public int getDirX() {
			return dirX;
		}
		
		public int getDirY() {
			return dirY;
		}
	}

